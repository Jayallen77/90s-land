const portalContent = document.querySelector('#portalContent');
const portalTitle = document.querySelector('#portalTitle');

function renderPortal(zoneKey) {
  if (!portalContent || !portalTitle) return;
  const zone = portalZones[zoneKey] || portalZones.internet;
  const shelfMarkup = zone.shelves ? `
    <div class="portal-shelves" aria-label="${zone.title} shelves">
      ${zone.shelves.map(shelf => `
        <section class="portal-box shelf-card">
          <h4>${shelf.title}</h4>
          <ul>${shelf.items.map(item => `<li>${item}</li>`).join('')}</ul>
        </section>
      `).join('')}
    </div>
  ` : '';

  portalTitle.textContent = zone.file;
  portalContent.innerHTML = `
    <p class="portal-kicker">${zone.kicker}</p>
    <h3>${zone.title}</h3>
    <p>${zone.intro}</p>
    <div class="portal-columns">
      <div class="portal-box">
        <h4>Time capsule</h4>
        <ul>${zone.history.map(item => `<li>${item}</li>`).join('')}</ul>
      </div>
      <div class="portal-box">
        <h4>Surf links</h4>
        ${zone.links.length ? `<ul class="portal-link-list">${zone.links.map(([label, url]) => `<li><a href="${url}" target="_blank" rel="noreferrer">${label}</a></li>`).join('')}</ul>` : '<p>Links coming soon. This rabbit hole is under construction.</p>'}
      </div>
    </div>
    ${shelfMarkup}
    <div class="portal-box">
      <h4>Do this like it is 1998</h4>
      <p class="portal-output">${zone.tryThis}</p>
      <div>${zone.prompts.map(prompt => `<span class="prompt-chip">${prompt}</span>`).join('')}</div>
    </div>
  `;
  document.querySelectorAll('[data-zone]').forEach(button => {
    if (button.classList.contains('portal-tab') || button.classList.contains('zone-card')) {
      button.classList.toggle('active', button.dataset.zone === zoneKey);
    }
  });
}

if (portalContent && portalTitle) {
  document.querySelectorAll('.portal-tab, .zone-card').forEach(button => {
    button.addEventListener('click', () => {
      renderPortal(button.dataset.zone);
      document.querySelector('#portal-window')?.scrollIntoView({ behavior: 'smooth', block: 'start' });
    });
  });
  renderPortal('internet');
}

const yearGrid = document.querySelector('#yearGrid');
const yearTitle = document.querySelector('#yearTitle');
const yearDetails = document.querySelector('#yearDetails');

function renderYear(year) {
  if (!yearTitle || !yearDetails) return;
  const data = years[year];
  if (!data) return;
  yearTitle.textContent = `${year}.LOG`;
  yearDetails.innerHTML = `
    <h3>${data.title}</h3>
    <p class="year-summary">${data.summary || data.vibe || ''}</p>
    ${data.categories ? `<div class="year-category-grid">${Object.entries(data.categories).map(([category, items]) => `<section class="year-category"><h4>${category}</h4><ul>${items.map(item => `<li>${item}</li>`).join('')}</ul></section>`).join('')}</div>` : `<ul>${(data.bullets || []).map(item => `<li>${item}</li>`).join('')}</ul>`}
    ${data.artifact ? `<p><b>Artifact:</b> ${data.artifact}</p>` : ''}
    ${data.vibe ? `<p><b>Vibe:</b> ${data.vibe}</p>` : ''}
  `;
  document.querySelectorAll('.year-grid button').forEach(button => {
    button.classList.toggle('active', button.dataset.year === String(year));
  });
}

if (yearGrid) {
  Object.keys(years).forEach(year => {
    const button = document.createElement('button');
    button.type = 'button';
    button.dataset.year = year;
    button.textContent = year;
    button.addEventListener('click', () => renderYear(year));
    yearGrid.appendChild(button);
  });
  renderYear(1990);
}

const form = document.querySelector('#guestbookForm');
const entries = document.querySelector('#guestbookEntries');
form?.addEventListener('submit', event => {
  event.preventDefault();
  const formData = new FormData(form);
  const handle = String(formData.get('handle')).trim().replace(/[<>]/g, '');
  const message = String(formData.get('message')).trim().replace(/[<>]/g, '');
  if (!handle || !message || !entries) return;
  const entry = document.createElement('div');
  entry.className = 'entry';
  entry.innerHTML = `<b>${handle}</b>: ${message}`;
  entries.prepend(entry);
  form.reset();
});

