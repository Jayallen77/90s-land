// Explore More Nostalgia filters
const resourceGrid = document.querySelector('#resourceGrid');
const resourceSearch = document.querySelector('#resourceSearch');
const resourceCount = document.querySelector('#resourceCount');
const resourceNoResults = document.querySelector('#resourceNoResults');
const resourceFilters = document.querySelectorAll('[data-resource-filter]');
let activeResourceFilter = 'all';

function updateResourceDirectory() {
  if (!resourceGrid) return;
  const cards = Array.from(resourceGrid.querySelectorAll('.resource-card'));
  const query = (resourceSearch?.value || '').trim().toLowerCase();
  let visible = 0;
  cards.forEach(card => {
    const matchesFilter = activeResourceFilter === 'all' || card.dataset.category === activeResourceFilter;
    const haystack = `${card.textContent} ${card.dataset.title || ''} ${card.dataset.tag || ''}`.toLowerCase();
    const matchesSearch = !query || haystack.includes(query);
    const show = matchesFilter && matchesSearch;
    card.classList.toggle('is-hidden', !show);
    if (show) visible += 1;
  });
  if (resourceCount) resourceCount.textContent = `Showing ${visible} nostalgia portal${visible === 1 ? '' : 's'}.`;
  if (resourceNoResults) resourceNoResults.hidden = visible !== 0;
}

if (resourceGrid) {
  const params = new URLSearchParams(window.location.search);
  const initialFilter = params.get('filter');
  if (initialFilter && Array.from(resourceFilters).some(button => button.dataset.resourceFilter === initialFilter)) {
    activeResourceFilter = initialFilter;
    resourceFilters.forEach(button => button.classList.toggle('active', button.dataset.resourceFilter === initialFilter));
  }

  resourceFilters.forEach(button => {
    button.addEventListener('click', () => {
      activeResourceFilter = button.dataset.resourceFilter || 'all';
      resourceFilters.forEach(btn => btn.classList.toggle('active', btn === button));
      updateResourceDirectory();
    });
  });
  resourceSearch?.addEventListener('input', updateResourceDirectory);
  updateResourceDirectory();
}
